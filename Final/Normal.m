% Press Addup
function pressAdd(handles)
v = str2num(get(handles.text3, 'String'));
set(handles.text3, 'String', v+1);

% Score Addup
function scoreAdd(handles)
v = str2num(get(handles.text4, 'String'));
set(handles.text4, 'String', v+1);
if v+1==18
    set(handles.text5, 'String', 'Ya');
end

% Press Button
function pressBtn(btn, hObject, handles)
global isFirst;
global useCard;
global prevBtn;
global prevhObject;
global puzzleAns;
pressAdd(handles);

if useCard(btn)==0
    toggleCard(btn, hObject);
    
    if isFirst==1
        prevBtn = btn;
        prevhObject = hObject;
    else
        if puzzleAns(btn)==puzzleAns(prevBtn)
            scoreAdd(handles);
        else
            pause(0.5);
            toggleCard(btn, hObject);
            toggleCard(prevBtn, prevhObject);
        end
    end
    
    if isFirst==1
        isFirst = 0;
    else
        isFirst = 1;
    end
end

function toggleCard(btn, hObject)
global useCard;
global puzzleAns;
if useCard(btn)==0
    set(hObject, 'String', puzzleAns(btn));
    useCard(btn) = 1;
else 
    set(hObject, 'String', '?');
    useCard(btn) = 0;
end
